    private void tanggalFormatFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tanggalFormatFieldFocusLost
        // TODO add your handling code here:
        String input = tanggalFormatField.getText();
        try {
            Date tanggalLahir = new SimpleDateFormat("dd-MM-yyyy").parse(input);
            int umur = hitungUmur(tanggalLahir);
            if (umur < 18) {
                JOptionPane.showMessageDialog(this, "Anda belum cukup umur untuk mendaftar pekerjaan ini", "Error", JOptionPane.ERROR_MESSAGE);
                tanggalFormatField.setText("");
            }
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Format tanggal tidak valid. Gunakan dd-MM-yyyy.", "Error", JOptionPane.ERROR_MESSAGE);
            tanggalFormatField.setText("");
        }
    }//GEN-LAST:event_tanggalFormatFieldFocusLost

    private void ipkFormatFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ipkFormatFieldKeyReleased
        // TODO add your handling code here:
        try {
            double ipk = Double.parseDouble(ipkFormatField.getText());
            if (ipk < 0.0 || ipk > 4.0) {
                JOptionPane.showMessageDialog(this, "IPK tidak valid. Masukkan nilai antara 0.0 hingga 4.0", "Error", JOptionPane.ERROR_MESSAGE);
                ipkFormatField.setText("");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "IPK tidak valid. Masukkan nilai numerik yang sesuai antara 0.0 hingga 4.0", "Error", JOptionPane.ERROR_MESSAGE);
            ipkFormatField.setText("");
        }
    }//GEN-LAST:event_ipkFormatFieldKeyReleased

