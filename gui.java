    private int hitungUmur(Date tanggalLahir) {
        Date tanggalSekarang = new Date();
        long selisih = tanggalSekarang.getTime() - tanggalLahir.getTime();
        return (int) (selisih / (1000L * 60 * 60 * 24 * 365));
    }

