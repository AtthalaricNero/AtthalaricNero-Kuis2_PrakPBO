    private void daftarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_daftarButtonActionPerformed
        // TODO add your handling code here:
        String nik = nikTextField.getText();
        String nama = namaTextField.getText();
        String alamat = alamatTextArea.getText();
        String pendidikan = pendidikanComboBox.getSelectedItem().toString(); // UPPERCASE agar memudahkan pengecekan
        String ipkText = ipkFormatField.getText();
        String tanggalLahir = tanggalFormatField.getText();
        try {
            double ipk = Double.parseDouble(ipkText); // Konversi input IPK ke double

            // Cek apakah memenuhi syarat pendidikan dan IPK
            if (!pendidikan.equals("D1") && !pendidikan.equals("D2")
                    && !pendidikan.equals("D3") && !pendidikan.equals("D4/S1") && !pendidikan.equals("S2") && !pendidikan.equals("S3")) {
                JOptionPane.showMessageDialog(this, "Tidak memenuhi pendidikan minimal D1",
                        "Pendaftaran Gagal", JOptionPane.ERROR_MESSAGE);
            } else if (ipk < 3.01) {
                JOptionPane.showMessageDialog(this, "Tidak memenuhi IPK minimal 3,01",
                        "Pendaftaran Gagal", JOptionPane.ERROR_MESSAGE);
            } else if (nama.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nama tidak Boleh Kosong",
                        "Pendaftaran Gagal", JOptionPane.ERROR_MESSAGE);
            } else if (nik.isEmpty()) {
                JOptionPane.showMessageDialog(this, "NIK tidak Boleh Kosong",
                        "Pendaftaran Gagal", JOptionPane.ERROR_MESSAGE);
            } else if (alamat.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Alamat tidak Boleh Kosong",
                        "Pendaftaran Gagal", JOptionPane.ERROR_MESSAGE);
            } else {
                // Jika memenuhi syarat, tampilkan pesan sukses
                JOptionPane.showMessageDialog(this, "Pendaftaran berhasil!",
                        "Sukses", JOptionPane.INFORMATION_MESSAGE);
                kosongkanForm();
            }
        } catch (NumberFormatException e) {
            // Jika IPK bukan angka, tampilkan pesan kesalahan
            JOptionPane.showMessageDialog(this, "Masukkan IPK yang valid.",
                    "Kesalahan Input", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_daftarButtonActionPerformed

